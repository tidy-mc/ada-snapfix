export function getWCAGInfo(wcagId: string): { title: string; url: string } | null {
  // Extract the SC number from various formats (e.g., "WCAG 2.1 - 2.4.4" -> "2.4.4")
  const match = wcagId.match(/(\d+\.\d+\.\d+)/);
  if (match) {
    const scId = match[1];
    return WCAG_MAP[scId] || null;
  }
  return null;
}

export function formatWCAGLink(wcagId: string): string {
  const wcagInfo = getWCAGInfo(wcagId);
  if (wcagInfo) {
    return `<a href="${wcagInfo.url}" target="_blank" style="color: #3b82f6; text-decoration: underline;">${wcagId} - ${wcagInfo.title}</a>`;
  }
  return wcagId;
}
